// 022 - 选择结构_单if-else if-else语句

public class Demo022IfElseifElse {
    public static void main(String[] args) {
        int num = -10;

        if (num > 0) {
            System.out.println("大于0");
        } else if (num < 0) {
            System.out.println("小于0");
        } else {
            System.out.println("等于0");
        }
    }
}
